
lista_numeros = [7, 1, 4, 9, 2, 5, 6, 3, 8] # Creramos una lista de números.

lista_numeros.sort() # Ordenamos la lista en orden ascendente
print("> Lista ordenada en orden ascendente:", lista_numeros) # Mostramos la lista en orden ascendente.

lista_numeros.sort(reverse=True) # Ordenamos la lista en orden descendente. Usando 'true' pero dentro añadiendo un parámetro llamado 'reverse' para hacerlo al contrario. Saliendo en descendente.
print("> Lista ordenada en orden descendente:", lista_numeros) # Mostramos la lista en orden descendente.

