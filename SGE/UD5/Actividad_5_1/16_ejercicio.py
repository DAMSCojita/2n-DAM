
lista_1 = [1, 2, 3, 4, 5] # Creamos la primera lista.

lista_2 = [6, 7, 8, 9, 10] # Creamos la segunda lista.

lista_1.extend(lista_2) # Agregamos la lista 2 a la lista 1.

print("> Lista combinada:", lista_1) # Mostramos la lista combinada.

print("> Lista invertida:", lista_1[::-1]) # Mostramos la lista invertida.
